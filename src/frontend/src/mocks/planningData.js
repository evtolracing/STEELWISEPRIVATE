// Helper function to calculate work center stats from jobs
export function getWorkCenterStats() {
  const stats = mockWorkCenters.map(wc => {
    const wcJobs = mockJobs.filter(j => j.workCenterId === wc.id);
    const activeJobs = wcJobs.filter(j => j.status === 'IN_PROCESS').length;
    const queuedJobs = wcJobs.filter(j => ['ORDERED', 'SCHEDULED'].includes(j.status)).length;
    
    return {
      ...wc,
      activeJobs,
      queuedJobs,
    };
  });
  
  return stats;
}
